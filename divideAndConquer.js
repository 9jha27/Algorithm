//Divide and Conquer
//Involves dividing a data set into smaller chunks and then repeating a process with a subset of data. 
//TREMENDOUSLY decreases time complexity

//Used in: Sorting algorithms, binary search, etc. 

//The idea: 
  //Take longer data (i.e. array) - start by dividing array into smaller pieces and doing something to each smaller piece. 
  //Divide larger chunks into smaller chunks

//EXAMPLE
//Given a sorted array of integers, write a function called search ,that accepts a value and returns the index where the value passed to the function is located. If the value is not found, return -1
//SORTED array! 
search([1,2,3,4,5,6],4) //3
